    public function listPdf(){
        date_default_timezone_set('America/Bogota');
        $teacher = User::role('Profesor')->get();
        $date = Carbon::now();
        $fechaHoy = $date->toDateTimeString();
        $pdf = Pdf::loadView('pages.pdf.teacherlist', compact('teacher','fechaHoy'));
        return $pdf->stream();
